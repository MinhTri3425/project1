public class Khoa_Phong {
    private String ma_khoa_phong;
    private String ten_khoa_phong;

    public Khoa_Phong(String ma_khoa_phong, String ten_khoa_phong) {
        this.ma_khoa_phong = ma_khoa_phong;
        this.ten_khoa_phong = ten_khoa_phong;
    }

    public String getMa_khoa_phong() {
        return ma_khoa_phong;
    }

    public void setMa_khoa_phong(String ma_khoa_phong) {
        this.ma_khoa_phong = ma_khoa_phong;
    }

    public String getTen_khoa_phong() {
        return ten_khoa_phong;
    }

    public void setTen_khoa_phong(String ten_khoa_phong) {
        this.ten_khoa_phong = ten_khoa_phong;
    }
    
    
}
